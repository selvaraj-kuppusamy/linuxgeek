import 'package:url_launcher/url_launcher.dart';

import '../utils/textstyle.dart';

class Gentoo extends StatefulWidget {
  const Gentoo({Key? key}) : super(key: key);

  @override
  State<Gentoo> createState() => _GentooState();
}

