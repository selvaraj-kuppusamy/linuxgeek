              'assets/distros/zorin/zorinlogo.png',
              height: 200.0,
              width: width,
            ),
            const SizedBox(height: 10.0,),
            RichText(
                textAlign: TextAlign.justify,
                text: TextSpan(children: [
                  TextSpan(
                      text: 'Zorin OS',
                      style: bluetext1,
                      recognizer: TapGestureRecognizer()
                        ..onTap = () => {
                          setState(() {
                            zorin(zorinwebsite);
                          }),
                        }),
                  const TextSpan(
                    text: '\t is a Linux distribution based on Ubuntu. '
                        'It uses a GNOME 3 or XFCE 4 desktop environment as default, '
                        'although the desktop is heavily customized in order'
